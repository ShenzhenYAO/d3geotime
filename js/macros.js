function getRecent(dataArray){
        // from the data array, and for each place, get the latest figure
        var places=[];
        var Recent1={};
        dataArray.forEach(d=>{
            // console.log(d.data)
            var theplace=d.data[6]
            if (! places.includes(theplace)){
                places.push(theplace)
                Recent1[theplace] = d
            } else {
                if (d.time > Recent1[theplace].time ) {
                    Recent1[theplace] = d
                }
            }
        })
        // console.log(Recent1)
        var gps=[];
        var Recent2={};
        places.forEach(d=>{
            // console.log(d.data)
            var thegps=Recent1[d].LatLng;
            if (! gps.includes(thegps)){
                gps.push(thegps)
                Recent2[thegps] = Recent1[d]
            } else {
                if ( Recent1[d].time > Recent2[thegps].time ) {
                    Recent2[thegps] = Recent1[d]
                }
            }
        })
        var recent=[]
        // console.log(Recent2)
        gps.forEach(d=>{
            if (! recent.includes(Recent2[d])){
                recent.push(Recent2[d])
            }        
        })
        return recent
}

